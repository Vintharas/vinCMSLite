        /// Default constructor
        /// </summary>
        public VinCmsMembershipProvider() : base()
        {
        }

        /// <summary>
        /// Class constructor. Initializes the User Repository
        /// </summary>
        public VinCmsMembershipProvider(IUserRepository userRepo) : base()
        {
            _userRepo = userRepo;
        }

