        /// Validates a given user, using a username and a password
        /// </summary>
        /// <param name="username"></param>
        /// <param name="password"></param>
        /// <returns></returns>
        public override bool ValidateUser(string username, string password)
        {
            return UserRepo.IsUserWithPassword(username, password);
