    /// <summary>
    /// Customized provider that manages authentication
    /// </summary>
    public class VinCmsMembershipProvider : MembershipProvider
    {

        private IUserRepository _userRepo;
        private IUserRepository UserRepo
        {
            get
            {
                if (_userRepo == null)
                {
                    _userRepo = (IUserRepository)vinCMS.MvcApplication.Container.GetService(typeof (IUserRepository));
                }
                return _userRepo;
            }
        }

